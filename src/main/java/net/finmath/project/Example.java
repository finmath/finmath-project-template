package net.finmath.project;

/**
 * This is an example class.
 * 
 * @author Christian Fries
 */
public class Example {

	private int value;

	/**
	 * Constructs an instance of the example with the given value.
	 * 
	 * @param value The given value.
	 */
	public Example(int value) {
		super();
		this.value = value;
	}

	/**
	 * Return the value.
	 * 
	 * @return The value
	 */
	public int getValue() {
		return value;
	}

	/**
	 * Set the value.
	 * 
	 * @param value the value to be setted to.
	 */
	public void setValue(int value) {
		this.value = value;
	}
	
}
